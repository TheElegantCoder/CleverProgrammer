import turtle

cherepashka = turtle.Turtle()

def square():
  cherepashka.forward(100)
  cherepashka.right(90)
  cherepashka.forward(100)
  cherepashka.right(90)
  cherepashka.forward(100)
  cherepashka.right(90)
  cherepashka.forward(100)
  cherepashka.right(90)

square()
cherepashka.backward(100)
square()

square()
cherepashka.forward(130)
square()

